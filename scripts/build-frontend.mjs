import * as esbuild from 'esbuild';
import * as path from 'path';
import * as fs from 'fs';

const SRC_DIR = path.resolve('frontend/src');
const OUT_DIR = path.resolve('frontend/js');
const isWatch = process.argv.includes('--watch');

// Only files that are loaded by the browser as named modules need to be
// explicit esbuild entry points.  Modules that are only statically imported
// are automatically bundled into shared chunk files by esbuild's code-splitting
// logic and do not need their own output file.
//
// A file must be listed here if it is:
//   (a) loaded directly via a <script> tag in the HTML, OR
//   (b) referenced by a dynamic import() call anywhere in the source tree.
//
// Dynamic imports in the source tree (grep `import(` in frontend/src/):
//   app.ts          → dataClient.ts, chart/DataChart.ts, chart/FftChart.ts,
//                     scatter/scatterPage.ts,
//                     drift/driftPage.ts,
//                     causal/causalPage.ts, causal/causalComparison.ts
//   scatter/export.ts → scatter/state.ts   (lazy self-reference inside subtree)
const ENTRY_MODULES = [
    'app.ts',
    'dataClient.ts',
    'chart/DataChart.ts',
    'chart/FftChart.ts',
    'scatter/scatterPage.ts',
    'scatter/state.ts',
];

const entryPoints = ENTRY_MODULES.map(f => path.join(SRC_DIR, f));

// Clean stale chunk files before building.
for (const f of fs.readdirSync(OUT_DIR).filter(n => n.startsWith('chunk-') && n.endsWith('.js'))) {
    fs.unlinkSync(path.join(OUT_DIR, f));
    const map = path.join(OUT_DIR, f + '.map');
    if (fs.existsSync(map)) fs.unlinkSync(map);
}

// Clean stale entry-point output files that are no longer in ENTRY_MODULES.
// These are non-chunk .js files whose corresponding .ts source is no longer
// a build entry point (they were generated by the old "all files" strategy).
const expectedOutputJs = new Set(ENTRY_MODULES.map(f => f.replace(/\.ts$/, '.js')));
function cleanStaleOutputs(dir, relBase) {
    for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
        const rel = relBase ? `${relBase}/${entry.name}` : entry.name;
        if (entry.isDirectory()) {
            cleanStaleOutputs(path.join(dir, entry.name), rel);
        } else if (entry.name.endsWith('.js') && !entry.name.startsWith('chunk-')) {
            if (!expectedOutputJs.has(rel)) {
                fs.unlinkSync(path.join(dir, entry.name));
                const mapPath = path.join(dir, `${entry.name}.map`);
                if (fs.existsSync(mapPath)) fs.unlinkSync(mapPath);
            }
        }
    }
}
cleanStaleOutputs(OUT_DIR, '');

const isProd = process.argv.includes('--prod');

const buildOptions = {
    entryPoints,
    outdir: OUT_DIR,
    format: 'esm',
    splitting: true,
    bundle: true,
    sourcemap: true,
    target: 'es2022',
    platform: 'browser',
    minify: isProd,
    treeShaking: true,
    external: [
        'https://unpkg.com/*',
    ],
    outExtension: { '.js': '.js' },
    logLevel: 'info',
    metafile: isProd,
};

if (isWatch) {
    const ctx = await esbuild.context(buildOptions);
    await ctx.watch();
    console.log('Watching for changes...');
} else {
    const result = await esbuild.build(buildOptions);
    if (isProd && result.metafile) {
        const analysis = await esbuild.analyzeMetafile(result.metafile);
        console.log(analysis);
    }
    console.log(`Frontend build complete${isProd ? ' (minified)' : ''}.`);
}
